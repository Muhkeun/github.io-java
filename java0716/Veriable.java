class Variable
{
    	//변수 - 하나의 값 메모리
	//선언(메모리 생성) => 대입 순으로 진행
	
	public static void main(String[] args) {
	
		//변수 a 선언
		int a;
		//변수에 상수 대입
		a = 7;
		
		//변수 b 선언과 대입을 한번에
		int b = 200;
		
		//변수 c, d를 선언
		int c,d;
		//변수 c, d에 상수 대입
		c = 50;
		d = 20000;
		
		//출력
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}
