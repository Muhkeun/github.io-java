class DataType 
{
	public static void main(String[] args) 
	{	
		//정수
		int a = 200;
		
		//논리형: true(1) / false(0)
		boolean b = false;
		
		//문자형
		char c = 'A';

		//실수형
		double d = 0; 

		//변수 출력
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}
