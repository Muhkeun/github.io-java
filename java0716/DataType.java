class DataType 
{
	public static void main(String[] args) 
	{	
		//정수
		int a = 200;
		//변수 출력 인터프리터 방식 (선언 후 바로 바로 출력. 오류 찾기 쉬워짐)
		System.out.println(a);
		
		//논리형: true(1) / false(0)
		boolean b = false;
		System.out.println(b);
		
		//문자형
		char c = 'A';
		System.out.println(c);
		
		//실수형
		double d = 0; 
		System.out.println(d);
		
	}
}
